
PUBLIC  DELAY2MS,DELAY10MS

DELAY_ASM 	SEGMENT		CODE
RSEG		DELAY_ASM

DELAY2MS:			;@12.000MHz	6T
	PUSH 30H
	PUSH 31H
	MOV 30H,#8
	MOV 31H,#192
NEXT:
	DJNZ 31H,NEXT
	DJNZ 30H,NEXT
	POP 31H
	POP 30H
	RET


DELAY10MS:   
	PUSH 	30H
	PUSH 	31H
    MOV 	31H	,	#0D7H
DL0:
    MOV 	30H	,	#2DH
    DJNZ 	30H	,	$
    DJNZ 	31H	,	DL0
	POP 	31H
	POP 	30H
    RET

END